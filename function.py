# Multiply function
def multiply(a,b):
    result = a * b
    return result

print(multiply(2,3))

# Addition function
def addition(a,b):
    result = a + b
    return result

print(addition(5, 5))

# Substration function
def substraction(a,b):
    result = a - b
    return result

print(substraction(10, 5))







